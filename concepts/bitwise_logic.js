// Bitwise AND (&): Performs a bitwise AND operation on each pair of corresponding bits. It returns a 1 if both bits are 1, otherwise 0.



// Bitwise OR (|): Performs a bitwise OR operation on each pair of corresponding bits. It returns a 1 if either of the bits is 1, otherwise 0.



// Bitwise XOR (^): Performs a bitwise XOR (exclusive OR) operation on each pair of corresponding bits. It returns a 1 if the bits are different, otherwise 0.



// Bitwise NOT (~): Inverts the bits of the operand. It changes each 1 to 0 and each 0 to 1.



// Left Shift (<<): Shifts the bits of the left operand to the left by the number of positions specified by the right operand. This is equivalent to multiplying the left operand by 2 raised to the power of the right operand.

// Right Shift (>>): Shifts the bits of the left operand to the right by the number of positions specified by the right operand. This is equivalent to dividing the left operand by 2 raised to the power of the right operand.

// Zero-fill Right Shift (>>>): Similar to the right shift operator (>>), but it always fills the shifted positions with zero. It effectively divides the left operand by 2 raised to the power of the right operand and discards the remainder.